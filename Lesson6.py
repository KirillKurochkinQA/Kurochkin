# Задача №13
# Дан список: ['понедельник', 'вторник', 'среда', 'четверг', 'пятница'].
# Используя цикл for, напечатай только те дни, в названии которых есть буква "р".
# Каждый подходящий день — на отдельной строке.
days = ['понедельник', 'вторник', 'среда', 'четверг', 'пятница']
