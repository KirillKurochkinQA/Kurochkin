# Задача №16
# Напиши цикл for, который пройдёт по числам от 1 до 20 включительно.
# Если число делится на 3, напечатай: "Делится на 3"
# Если число делится на 5, напечатай: "Делится на 5"
# Если число делится и на 3, и на 5, напечатай: "Делится и на 3, и на 5"
# Во всех остальных случаях — ничего не печатай.
for number in range(1, 21):
    if number % 3 == 0 and number % 5 == 0:
        print("Делится и на 3, и на 5")
    elif number % 3 == 0:
        print("Делится на 3")
    elif number % 5 == 0:
        print("Делится на 5")

