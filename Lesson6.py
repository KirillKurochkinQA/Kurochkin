# Задача №8
# Используя цикл while, напечатай все числа от 1 до 15, кроме тех, которые делятся на 5.
# Каждое подходящее число выводи на отдельной строке.
number = 1
while number <= 15:
    if number % 5 != 0:
        print(number)
    number += 1

