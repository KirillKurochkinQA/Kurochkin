# Задача №11
# Напиши цикл while, который будет запрашивать у пользователя ввод числа с помощью input(),
# пока пользователь не введёт число 0.
# После каждого ввода программа должна печатать:
# Вы ввели: <число>
# Как только введено 0 — цикл завершается, и программа ничего больше не делает.
while True:
    number = int(input())
    print("Вы ввели число: ", number)
    if number == 0:
        break

# Задача №12
