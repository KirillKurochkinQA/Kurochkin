# Задача №1
# Напечатай все числа от 1 до 5 включительно, используя цикл for и функцию range.
# Каждое число должно выводиться на отдельной строке.
for number in range(1,6):
    print(number)


# Задача №2
# Создай список с названиями трёх фруктов (например, ['яблоко', 'банан', 'апельсин']).
# Используя цикл for, напечатай каждое название на отдельной строке.
fruits = ["яблоко", "банан", "апельсин"]
for fruit in fruits:
    print(fruit)

# Задача №3
# Используя цикл while, напечатай числа от 10 до 1 включительно в обратном порядке.
# Каждое число должно выводиться на отдельной строке.
count = 10
while count > 0:
    print(count)
    count -= 1

# Задача №4
