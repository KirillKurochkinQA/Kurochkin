# Задача №5
# Напиши цикл for, который пройдёт по числам от 0 до 9 (включительно), но остановится, как только встретит число 7.
# Выводи каждое число до остановки (включая 7), по одному на строке.
# Используй для остановки цикла оператор break.
for number in range(0, 10):
    print(number)
    if number == 7:
        break

# Задача №6
