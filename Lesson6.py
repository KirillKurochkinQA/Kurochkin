# Задача №15
# Дан список: [10, 3, 7, 12, 5, 8].
# Используя цикл for, посчитай сумму всех чисел, которые меньше 10.
# После окончания цикла напечатай получившуюся сумму.
numbers = [10, 3, 7, 12, 5, 8]
result = 0
for number in numbers:
    if number < 10:
        result += number
print(result)
